package polymorphism01;

public class LG_TV {
	public void  on() {
		System.out.println("LG TV 전원 On");
	}
	public void off() {
		System.out.println("LG TV 전원 Off");
	}
	public void soundUp() {
		System.out.println("LG TV 소리 크게~");
	}
	public void soundDown() {
		System.out.println("LG TV 소리 작게~");
	}
}
